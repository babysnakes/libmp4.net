namespace libmp4.net
{
    public enum VideoResolution : byte
    {
        /// <summary>
        /// Not HD
        /// </summary>
        SD = 0,

        /// <summary>
        /// 720
        /// </summary>
        HD = 1,

        /// <summary>
        /// 1080
        /// </summary>
        FHD = 2,

        /// <summary>
        /// 4K
        /// </summary>
        UHD = 3,

        /// <summary>
        /// 8K
        /// </summary>
        UHD2 = 4
    }
}
